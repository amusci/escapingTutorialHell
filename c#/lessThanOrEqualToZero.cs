    public static bool lessThanOrEqualToZero(double a) 
    /* Create a function that takes a number as its only argument and returns true if it's less than or equal to zero, otherwise return false. */
    {

      if (a > 0)
      {
        return false;

      }
      else if (a <= 0)
      {

        return true;

      }

      return false;
    	
    }