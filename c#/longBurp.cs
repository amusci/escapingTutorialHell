	public static string LongBurp(int b) 
  /* Create a function that returns the string "Burp" with the amount of "r's" determined by the input parameters of the function. */
	  {

      string bu = "Bu";

      for (int i = 0; i < b; i++) {

        bu += "r";


      }

      return bu + "p";
		
	  }